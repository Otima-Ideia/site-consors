                    <p>Os consórcios imobiliários trazem uma vantagem muito além do que é oferecido pelos financiamentos. Além de não cobrar juros do cotista, esse tipo de negócio permite o uso do Fundo de Garantia por Tempo de Serviço (FGTS) para abatimento do valor contratado, principalmente quando a carta de crédito já foi contemplada. </p>
