<div id="collapse10" class="panel-collapse collapse">
<div class="panel-body">
<p>
É a primeira assembléia geral ordinária do seu grupo. A administradora só pode convocá-la depois da adesão de 70% dos participantes previstos para o grupo e após 8 dias da última adesão. <br>
Se a administradora não aprovar a constituição do grupo até 90 dias depois de sua adesão, deverá devolver ao consorciado, integralmente, todos os valores pagos, acrescidos dos rendimentos líquidos provenientes de sua aplicação financeira. <br>
Várias decisões importantes são tomadas pelos consorciados presentes nessa primeira assembléia. <br>
Representantes do grupo: todo grupo de consórcio deve ter no mínimo 3 representantes, escolhidos entre os consorciados não contemplados. Os representantes auxiliam na fiscalização dos atos da administradora e têm acesso a todos os demonstrativos e documentos pertinentes às operações do grupo. <br>
Lista de participantes: a administradora é obrigada a colocar à disposição de todos os consorciados uma relação com nome, endereço e telefone de todos os participantes do grupo. A lista atualizada deve permanecer à disposição dos participantes em todas as assembléias, conforme Circular 3.084, de 2002, que alterou o regulamento anexo à Circular 2.766, de 1997. O consorciado que não desejar ter seu nome divulgado deverá comunicar à administradora por escrito. <br>
Assembléias gerais <br>
É na assembléia geral que você pode saber a situação de seu grupo. <br>
É realizada todo mês, em dia, hora e local previamente determinados. Podem votar todos os participantes em dia com sua mensalidade. As decisões são tomadas por maioria dos votos dos presentes, não se computando os votos em branco. <br>
Assembléias gerais extraordinárias <br>
Se for do interesse do grupo, a administradora ou os consorciados podem convocar assembléias gerais extraordinárias.
</p>
