Quais são os tipos de assembléia? Assembléia de constituição
<img src="<?php echo get_template_directory_uri(); ?>/images/icon-bg.png" alt="icon" class="active">
<img src="<?php echo get_template_directory_uri(); ?>/images/icon-bg-hover.png" alt="icon" class="hover">
</a>
